package util;

/**
 *
 * @author Freddy
 */
public class Cocina {

    private String nombre;
    private String coementario;

    public Cocina() {
    }

    public Cocina(String nombre, String coementario) {
        this.nombre = nombre;
        this.coementario = coementario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCoementario() {
        return coementario;
    }

    public void setCoementario(String coementario) {
        this.coementario = coementario;
    }

    
    
}
